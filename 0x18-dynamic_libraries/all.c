#include <stdlib.h>

/**
 * mul - mul of two integers
 * @a: first unt
 * @b: second int
 * Return: addition
 */
int mul(int a, int b)
{
	return (a * b);
}


/**
 * sub - substract two integers
 * @a: first unt
 * @b: second int
 * Return: substracted final result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - divide two integers
 * @a: first unt
 * @b: second int
 * Return: divison final result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod of two integers
 * @a: first unt
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a: first unt
 * @b: second int
 * Return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}
